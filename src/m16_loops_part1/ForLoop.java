package m16_loops_part1;

public class ForLoop {
    public static void main(String[] args) {
        for( int i = 0 ; i < 5 ; i++){
            System.out.println("Hello World");
        }

        for (int i = 0; i < 10; i++) {
            System.out.print(i + " " );
        }
        System.out.println("hallo");
    }
}
