package m13_operators_part3;

public class RelationalsOperators {
    public static void main(String[] args) {

        int x = 0;
        System.out.println(x < 1); //true
        System.out.println(x > 0);
        System.out.println( x >= 0);
        System.out.println(x <= 10);
        System.out.println(x <= -10);
        System.out.println(x == 0);
        System.out.println(x != 0);
        System.out.println("Cydeo" == "cydeo"); //f
        System.out.println('A' != 'a');         //t
        System.out.println(1.0 == 1);           //t


    }
}
