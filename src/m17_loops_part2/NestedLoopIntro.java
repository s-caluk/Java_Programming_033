package m17_loops_part2;

public class NestedLoopIntro {
    public static void main(String[] args) {
        for (int j = 0; j < 5; j++) {       //X

            for (int i = 1; i < 6; i++) {   //Y
                System.out.print(i + " ");
            }

            System.out.println();
        }

    }
}
